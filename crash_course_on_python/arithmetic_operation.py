# Arithmetic operations in Python

# Input values
a = 15
b = 4

# Addition
sum_result = a + b
print(f"{a} + {b} = {sum_result}") # 15 + 4 = 19

# Subtraction
sub_result = a - b
print(f"{a} - {b} = {sub_result}") # 15 - 4 = 11

# Multiplication
mul_result = a * b
print(f"{a} * {b} = {mul_result}") # 15 * 4 = 60

# Division (floating point)
div_result = a / b
print(f"{a} / {b} = {div_result}") # 15 / 4 = 3.75

# Floor Division (integer result)
floor_div_result = a // b
print(f"{a} // {b} = {floor_div_result}") # 15 // 4 = 3

# Modulus (remainder)
mod_result = a % b
print(f"{a} % {b} = {mod_result}") # 15 % 4 = 3

# Exponentiation (power)
exp_result = a ** b
print(f"{a} ** {b} = {exp_result}") # 15 ** 4 = 50625


